// We are creating Models Or Schema Here .

const { uniq } = require("lodash");
const mongoose = require("mongoose");

// Defined the Person Schema .
const personSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  age: {
    type: Number,
  },
  work: {
    type: String,
    enum: ["chef", "waiter", "manager"],
    required: true,
  },
  mobile: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  address: {
    type: String,
  },
  salary: {
    type: Number,
    required: true,
  },
});

// Createn Person model
const Person = mongoose.model('Person',personSchema)
module.exports = Person;